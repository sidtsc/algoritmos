from math import *

print("Math module")

print("Sine function of pi/2: ", sin(pi/2))
print("Cosine function of 0: ", cos(0))
print("Tangent function of pi/4: ", tan(pi/4))

print("Arc sine function of 0: ", asin(0))
print("Arc cosine function of 1: ", acos(1))
print("Arc tangent function of 0: ", atan(0))

print("Square root of 625: ", sqrt(625))

print("2 raised power 8: ", pow(2,8))
print("Logarithm of 100 (base 10): ", log10(100))

print("Exponential of 1 (base e): ", exp(1))
print("Logarithm of 100 (base e): ", log(100))

print("Absolute value of -9.2: ", fabs(-9.2))

print("Rounds to smallest integer not less than 6.7: ", ceil(6.7))
print("Rounds to largest integer not greater than 6.7: ", floor(6.7))
