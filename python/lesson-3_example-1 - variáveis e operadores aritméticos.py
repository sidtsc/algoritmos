print("Test using variables and arithmetics operators")

number1 = input("Enter first number:\n")
number1 = int(number1)

number2 = input("Enter second number:\n")
number2 = int(number2)

sum = number1 + number2
print ("The sum is ", sum)

subtraction = number1 - number2
print ("The subtraction is ", subtraction)

multiplication = number1 * number2
print ("The multiplication is ", multiplication)

division = number1 / number2
print ("The division is ", division)

exponentiation = number1 ** number2
print ("The exponentiation is ", exponentiation)

modulus = number1 % number2
print ("The modulus is ", modulus)
